
public class BattleShip
	{
		private int size; 
		private String name;
		public BattleShip(int a, String b){
			size = a;
			name = b;
		}
		public int getSize()
			{
				return size;
			}
		public void setSize(int size)
			{
				this.size = size;
			}
		public String getName()
			{
				return name;
			}
		public void setName(String name)
			{
				this.name = name;
			}
		
	}
